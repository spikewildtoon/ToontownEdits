def timeElapsedString(timeDelta):
    timeDelta = abs(timeDelta)
    if timeDelta.days > 0:
        if timeDelta.days == 1:
            return '1 dzień temu'
        else:
            return '%s dni temu' % timeDelta.days
    elif timeDelta.seconds / 3600 > 0:
        if timeDelta.seconds / 3600 == 1:
            return '1 godzinę temu'
        elif timeDelta.seconds / 3600 % 100 in [11, 12, 13, 14] or timeDelta.seconds / 3600 % 10 in [0, 1]:
            return '%s godzin temu' % timeDelta.seconds / 3600
        elif timeDelta.seconds / 3600 % 10 < 5:
            return '%s godziny temu' % timeDelta.seconds / 3600
        else:
            return '%s godzin temu' % timeDelta.seconds / 3600
    elif timeDelta.seconds / 60 < 60:
        if timeDelta.seconds / 60 < 2:
            return '1 minutę temu'
        elif timeDelta.seconds /60 % 100 in [11, 12, 13, 14] or timeDelta.seconds / 3600 % 10 in [0, 1]:
            return '%s minut temu' % timeDelta.seconds / 60
        elif timeDelta.seconds / 60 % 10 < 5:
            return '%s minuty temu' % timeDelta.seconds / 60
        else:
            return '%s minut temu' % timeDelta.seconds / 60
        
